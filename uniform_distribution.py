# A spinner is divided into 8 equal sections labeled 1 to 8.
# What is the probability of landing on an even number?


# Probability of landing on an even number in a uniform distribution
total_outcomes = 8
even_outcomes = 4  # (2, 4, 6, 8)

probability_even = even_outcomes / total_outcomes
print("Probability of landing on an even number:", probability_even)
