# A factory produces light bulbs with lifespans following a normal distribution:

# Mean lifespan = 1,000 hours
# Standard deviation = 100 hours
# According to the 68-95-99.7 Rule, what percentage of bulbs last between 900 and 1,100 hours?



# 68% rule: 1 standard deviation from the mean
print("Percentage of bulbs lasting between 900 and 1100 hours: 68%")
print("Percentage of bulbs lasting between 800 and 1200 hours: 95%")
print("Percentage of bulbs lasting between 700 and 1300 hours: 99.7%")