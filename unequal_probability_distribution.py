# A box contains 10 balls:

# 4 white
# 3 black
# 3 red
# If you randomly pick one ball, find the probability of selecting:

# A white ball
# A black ball
# A red ball



# Given ball counts
total_balls = 10
white_balls = 4
black_balls = 3
red_balls = 3

# Calculating probabilities
prob_white = white_balls / total_balls
prob_black = black_balls / total_balls
prob_red = red_balls / total_balls

print("Probability of White Ball:", prob_white)
print("Probability of Black Ball:", prob_black)
print("Probability of Red Ball:", prob_red)
